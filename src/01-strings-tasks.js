/**
 * Returns the string representation of rectangle with specified width and height
 * using pseudograhic chars
 *
 * @param {number} width
 * @param {number} height
 * @return {string}
 *
 * @example
 *
 *            '┌────┐\n'+
 *  (6,4) =>  '│    │\n'+
 *            '│    │\n'+
 *            '└────┘\n'
 *
 *  (2,2) =>  '┌┐\n'+
 *            '└┘\n'
 *
 *             '┌──────────┐\n'+
 *  (12,3) =>  '│          │\n'+
 *             '└──────────┘\n'
 *
 */
function getRectangleString(width, height) {
  const firstRow = `┌${'─'.repeat(width - 2)}┐\n`;
  const middleRow = `│${' '.repeat(width - 2)}│\n`;
  const lastRow = `└${'─'.repeat(width - 2)}┘\n`;
  const amountMiddleRow = height - 2;
  const rectangle = `${firstRow}${middleRow.repeat(amountMiddleRow)}${lastRow}`;
  return rectangle;
}


/**
 * Encode specified string with ROT13 cipher
 * See details:  https://en.wikipedia.org/wiki/ROT13
 *
 * @param {string} str
 * @return {string}
 *
 * @example
 *
 *   'hello' => 'uryyb'
 *   'Why did the chicken cross the road?' => 'Jul qvq gur puvpxra pebff gur ebnq?'
 *   'Gb trg gb gur bgure fvqr!' => 'To get to the other side!'
 *   'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz'
 *    => 'NOPQRSTUVWXYZABCDEFGHIJKLMnopqrstuvwxyzabcdefghijklm'
 *
 */
function encodeToRot13(str) {
  const arrAlph = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz.!?'.split('');
  const arrRotate = 'NOPQRSTUVWXYZABCDEFGHIJKLMnopqrstuvwxyzabcdefghijklm.!?'.split('');
  const arrOfStr = str.split('');

  const indexesFromAlf = arrOfStr.map((item) => {
    const indexOfArrStr = arrAlph.indexOf(item);
    return indexOfArrStr;
  });

  const newArr = indexesFromAlf.map((index) => {
    const arrFromRotate = arrRotate[index];
    return arrFromRotate;
  });

  for (let i = 0; i < newArr.length; i += 1) {
    if (newArr[i] === undefined) {
      newArr[i] = ' ';
    }
  }
  return newArr.join('');
}

/**
 * Returns true if the value is string; otherwise false.
 * @param {string} value
 * @return {boolean}
 *
 * @example
 *   isString() => false
 *   isString(null) => false
 *   isString([]) => false
 *   isString({}) => false
 *   isString('test') => true
 *   isString(new String('test')) => true
 */
function isString(value) {
  const firstConditional = typeof value === 'string';
  const secondConditional = value instanceof String === true;
  return firstConditional || secondConditional;
}


